/** カタカナ → ひらがな変換 */
function katakanaToHiragana(str) {
  return str.replace(/[\u30A1-\u30F6]/g, (ch) =>
    String.fromCharCode(ch.charCodeAt(0) - 0x60)
  )
}

/** 文字列がカナのみか判定（英数字・記号は除く） */
function isKanaOnly(str) {
  return /^[\u3040-\u309F\u30A0-\u30FFー]+$/.test(str)
}

/** 単語の読みを自動生成 */
export function autoYomi(word) {
  if (!word) return ''

  // 辞書にあればそのまま返す
  if (KANJI_YOMI[word]) return KANJI_YOMI[word]

  // カナのみならひらがなに変換
  if (isKanaOnly(word)) return katakanaToHiragana(word)

  // 不明な語
  return ''
}
